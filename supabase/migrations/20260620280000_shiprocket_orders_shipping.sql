CREATE OR REPLACE FUNCTION public.is_india_origin_seller(p_seller_user_id UUID)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1
    FROM public.seller_accounts sa
    JOIN public.shipping_provider_settings sps ON sps.provider = 'shiprocket' AND sps.is_enabled
    WHERE sa.user_id = p_seller_user_id
      AND sa.iso_alpha2 = sps.origin_country_iso2
  );
$$;

CREATE OR REPLACE FUNCTION public.resolve_shipping_lane(
  p_origin_iso2 CHAR(2),
  p_delivery_iso2 CHAR(2)
)
RETURNS public.shipping_lane
LANGUAGE plpgsql
STABLE
SET search_path = public
AS $$
BEGIN
  IF p_origin_iso2 IS DISTINCT FROM (SELECT origin_country_iso2 FROM public.shipping_provider_settings WHERE provider = 'shiprocket' AND is_enabled) THEN
    RAISE EXCEPTION 'Only India-origin seller shipments are supported.';
  END IF;

  IF p_delivery_iso2 = p_origin_iso2 THEN
    RETURN 'india_domestic';
  END IF;

  RETURN 'india_international';
END;
$$;

CREATE OR REPLACE FUNCTION public.generate_marketplace_order_number()
RETURNS TEXT
LANGUAGE plpgsql
SET search_path = public
AS $$
DECLARE
  v_seq BIGINT;
BEGIN
  SELECT COALESCE(MAX(id), 0) + 1 INTO v_seq FROM public.marketplace_orders;
  RETURN 'AGT-' || TO_CHAR(NOW() AT TIME ZONE 'UTC', 'YYYYMMDD') || '-' || LPAD(v_seq::TEXT, 6, '0');
END;
$$;

CREATE OR REPLACE FUNCTION public.create_marketplace_order(
  p_seller_user_id UUID,
  p_payment_method public.marketplace_payment_method,
  p_currency_code CHAR(3),
  p_subtotal NUMERIC,
  p_shipping_amount NUMERIC,
  p_cod_charges_amount NUMERIC,
  p_tax_amount NUMERIC,
  p_total_amount NUMERIC,
  p_delivery JSONB,
  p_shipping_quote JSONB,
  p_items JSONB
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_buyer UUID := auth.uid();
  v_order_id BIGINT;
  v_order_number TEXT;
  v_lane public.shipping_lane;
  v_origin_iso2 CHAR(2);
  v_pickup_postcode TEXT;
  v_item JSONB;
  v_product public.seller_products%ROWTYPE;
BEGIN
  IF v_buyer IS NULL THEN
    RAISE EXCEPTION 'Authentication required.';
  END IF;

  IF NOT public.is_india_origin_seller(p_seller_user_id) THEN
    RAISE EXCEPTION 'Shiprocket shipping is only available for India-origin sellers.';
  END IF;

  SELECT sa.iso_alpha2 INTO v_origin_iso2
  FROM public.seller_accounts sa
  WHERE sa.user_id = p_seller_user_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Seller account not found.';
  END IF;

  SELECT w.postal_code
  INTO v_pickup_postcode
  FROM public.seller_warehouses w
  WHERE w.user_id = p_seller_user_id
    AND w.is_completed = true;

  IF v_pickup_postcode IS NULL THEN
    RAISE EXCEPTION 'Seller warehouse pickup postcode is not configured.';
  END IF;

  v_lane := public.resolve_shipping_lane(v_origin_iso2, upper(btrim(p_delivery->>'countryIso2'))::CHAR(2));

  IF v_lane = 'india_international' AND p_payment_method = 'cod' THEN
    RAISE EXCEPTION 'Cash on delivery is not available for international shipments.';
  END IF;

  IF v_lane = 'india_domestic' AND p_payment_method = 'cod' AND COALESCE((p_shipping_quote->>'codAvailable')::BOOLEAN, false) IS NOT TRUE THEN
    RAISE EXCEPTION 'Cash on delivery is not serviceable for this domestic address.';
  END IF;

  v_order_number := public.generate_marketplace_order_number();

  INSERT INTO public.marketplace_orders (
    order_number,
    buyer_user_id,
    seller_user_id,
    payment_method,
    shipping_lane,
    currency_code,
    subtotal_amount,
    shipping_amount,
    cod_charges_amount,
    tax_amount,
    total_amount,
    pickup_postcode,
    delivery_full_name,
    delivery_phone,
    delivery_email,
    delivery_address_line1,
    delivery_address_line2,
    delivery_city,
    delivery_state,
    delivery_postcode,
    delivery_country_iso2,
    package_weight_kg,
    package_length_cm,
    package_width_cm,
    package_height_cm,
    shipping_courier_company_id,
    shipping_courier_name,
    shipping_estimated_delivery
  )
  VALUES (
    v_order_number,
    v_buyer,
    p_seller_user_id,
    p_payment_method,
    v_lane,
    upper(p_currency_code),
    p_subtotal,
    p_shipping_amount,
    COALESCE(p_cod_charges_amount, 0),
    p_tax_amount,
    p_total_amount,
    v_pickup_postcode,
    btrim(p_delivery->>'fullName'),
    btrim(p_delivery->>'phone'),
    lower(btrim(p_delivery->>'email')),
    btrim(p_delivery->>'addressLine1'),
    NULLIF(btrim(p_delivery->>'addressLine2'), ''),
    btrim(p_delivery->>'city'),
    btrim(p_delivery->>'state'),
    btrim(p_delivery->>'postcode'),
    upper(btrim(p_delivery->>'countryIso2'))::CHAR(2),
    (p_shipping_quote->>'weightKg')::NUMERIC,
    NULLIF(p_shipping_quote->>'lengthCm', '')::NUMERIC,
    NULLIF(p_shipping_quote->>'widthCm', '')::NUMERIC,
    NULLIF(p_shipping_quote->>'heightCm', '')::NUMERIC,
    NULLIF(p_shipping_quote->>'courierCompanyId', '')::BIGINT,
    NULLIF(p_shipping_quote->>'courierName', ''),
    NULLIF(p_shipping_quote->>'estimatedDelivery', '')
  )
  RETURNING id INTO v_order_id;

  FOR v_item IN SELECT * FROM jsonb_array_elements(p_items)
  LOOP
    SELECT * INTO v_product
    FROM public.seller_products
    WHERE id = (v_item->>'productId')::BIGINT
      AND user_id = p_seller_user_id
      AND approval_status = 'approved';

    IF NOT FOUND THEN
      RAISE EXCEPTION 'Product % is not available from this seller.', v_item->>'productId';
    END IF;

    INSERT INTO public.marketplace_order_items (
      order_id,
      product_id,
      variant_id,
      product_name,
      sku,
      hsn_code,
      quantity,
      unit_price,
      line_total
    )
    VALUES (
      v_order_id,
      v_product.id,
      NULLIF(v_item->>'variantId', '')::BIGINT,
      v_product.product_name,
      COALESCE(NULLIF(v_item->>'sku', ''), v_product.sku),
      v_product.hsn_code,
      (v_item->>'quantity')::INT,
      (v_item->>'unitPrice')::NUMERIC,
      (v_item->>'lineTotal')::NUMERIC
    );
  END LOOP;

  RETURN jsonb_build_object(
    'ok', true,
    'orderId', v_order_id,
    'orderNumber', v_order_number,
    'shippingLane', v_lane::TEXT
  );
END;
$$;

CREATE OR REPLACE FUNCTION public.seller_respond_marketplace_order(
  p_order_id BIGINT,
  p_accept BOOLEAN,
  p_note TEXT DEFAULT NULL
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_order public.marketplace_orders%ROWTYPE;
BEGIN
  SELECT * INTO v_order
  FROM public.marketplace_orders
  WHERE id = p_order_id
    AND seller_user_id = auth.uid();

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Order not found.';
  END IF;

  IF v_order.status <> 'pending_seller_acceptance' THEN
    RAISE EXCEPTION 'Order is not awaiting seller response.';
  END IF;

  UPDATE public.marketplace_orders
  SET
    status = CASE WHEN p_accept THEN 'seller_accepted' ELSE 'seller_rejected' END,
    seller_response_note = NULLIF(btrim(p_note), ''),
    seller_responded_at = NOW(),
    updated_at = NOW()
  WHERE id = p_order_id;

  RETURN jsonb_build_object(
    'ok', true,
    'status', CASE WHEN p_accept THEN 'seller_accepted' ELSE 'seller_rejected' END
  );
END;
$$;

CREATE OR REPLACE FUNCTION public.admin_mark_shiprocket_pending(p_order_id BIGINT)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_order public.marketplace_orders%ROWTYPE;
BEGIN
  IF NOT public.is_admin_account() THEN
    RAISE EXCEPTION 'Admin access required.';
  END IF;

  SELECT * INTO v_order FROM public.marketplace_orders WHERE id = p_order_id;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Order not found.';
  END IF;

  IF v_order.status <> 'seller_accepted' THEN
    RAISE EXCEPTION 'Order must be seller-accepted before Shiprocket push.';
  END IF;

  UPDATE public.marketplace_orders
  SET status = 'shiprocket_pending', updated_at = NOW()
  WHERE id = p_order_id;

  RETURN jsonb_build_object('ok', true, 'status', 'shiprocket_pending');
END;
$$;

CREATE OR REPLACE FUNCTION public.admin_complete_shiprocket_push(
  p_order_id BIGINT,
  p_shiprocket_order_id BIGINT,
  p_shiprocket_shipment_id BIGINT,
  p_awb_code TEXT,
  p_label_url TEXT DEFAULT NULL,
  p_manifest_url TEXT DEFAULT NULL,
  p_courier_name TEXT DEFAULT NULL
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  IF NOT public.is_admin_account() THEN
    RAISE EXCEPTION 'Admin access required.';
  END IF;

  UPDATE public.marketplace_orders
  SET
    status = 'shiprocket_created',
    admin_shiprocket_pushed_at = NOW(),
    admin_shiprocket_pushed_by = auth.uid(),
    shipping_courier_name = COALESCE(p_courier_name, shipping_courier_name),
    updated_at = NOW()
  WHERE id = p_order_id
    AND status IN ('seller_accepted', 'shiprocket_pending');

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Order is not ready for Shiprocket completion.';
  END IF;

  INSERT INTO public.marketplace_order_shipments (
    order_id,
    provider,
    shiprocket_order_id,
    shiprocket_shipment_id,
    awb_code,
    label_url,
    manifest_url
  )
  VALUES (
    p_order_id,
    'shiprocket',
    p_shiprocket_order_id,
    p_shiprocket_shipment_id,
    NULLIF(btrim(p_awb_code), ''),
    NULLIF(btrim(p_label_url), ''),
    NULLIF(btrim(p_manifest_url), '')
  )
  ON CONFLICT (order_id) DO UPDATE
  SET
    shiprocket_order_id = EXCLUDED.shiprocket_order_id,
    shiprocket_shipment_id = EXCLUDED.shiprocket_shipment_id,
    awb_code = EXCLUDED.awb_code,
    label_url = COALESCE(EXCLUDED.label_url, marketplace_order_shipments.label_url),
    manifest_url = COALESCE(EXCLUDED.manifest_url, marketplace_order_shipments.manifest_url),
    updated_at = NOW();

  RETURN jsonb_build_object('ok', true, 'status', 'shiprocket_created');
END;
$$;

CREATE OR REPLACE FUNCTION public.seller_mark_order_packed(p_order_id BIGINT)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  UPDATE public.marketplace_orders
  SET status = 'packed', packed_at = NOW(), updated_at = NOW()
  WHERE id = p_order_id
    AND seller_user_id = auth.uid()
    AND status = 'shiprocket_created';

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Order must be Shiprocket-created before marking packed.';
  END IF;

  RETURN jsonb_build_object('ok', true, 'status', 'packed');
END;
$$;

CREATE OR REPLACE FUNCTION public.update_shipment_documents(
  p_order_id BIGINT,
  p_label_url TEXT,
  p_manifest_url TEXT
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  UPDATE public.marketplace_order_shipments
  SET
    label_url = NULLIF(btrim(p_label_url), ''),
    manifest_url = NULLIF(btrim(p_manifest_url), ''),
    updated_at = NOW()
  WHERE order_id = p_order_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Shipment not found.';
  END IF;

  RETURN jsonb_build_object('ok', true);
END;
$$;

CREATE OR REPLACE FUNCTION public.update_shipment_tracking(
  p_order_id BIGINT,
  p_tracking_payload JSONB,
  p_mark_shipped BOOLEAN DEFAULT false
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  UPDATE public.marketplace_order_shipments
  SET
    tracking_payload = p_tracking_payload,
    last_tracked_at = NOW(),
    updated_at = NOW()
  WHERE order_id = p_order_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Shipment not found.';
  END IF;

  IF p_mark_shipped THEN
    UPDATE public.marketplace_orders
    SET status = 'shipped', updated_at = NOW()
    WHERE id = p_order_id
      AND status IN ('packed', 'shiprocket_created');
  END IF;

  RETURN jsonb_build_object('ok', true);
END;
$$;

