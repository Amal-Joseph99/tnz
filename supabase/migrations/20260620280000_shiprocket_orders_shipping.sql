  provider TEXT PRIMARY KEY,
  origin_country_iso2 CHAR(2) NOT NULL,
  api_base_url TEXT NOT NULL,
  domestic_serviceability_path TEXT NOT NULL,
  international_serviceability_path TEXT NOT NULL,
  auth_login_path TEXT NOT NULL,
  create_order_path TEXT NOT NULL,
  assign_awb_path TEXT NOT NULL,
  generate_label_path TEXT NOT NULL,
  generate_manifest_path TEXT NOT NULL,
  print_manifest_path TEXT NOT NULL,
  track_awb_path_template TEXT NOT NULL,
